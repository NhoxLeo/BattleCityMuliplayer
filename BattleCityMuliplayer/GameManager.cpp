void GameManager::UpdateColl(Tank* tank,Bullet* bullet)
{
	if (tank->getTouchAbleState())
	{
		if (tank->getCamp() != bullet->getCamp())
		{
			if (!tank->getInvincible())
			{
				GameManager::getInstance()->addGrade(bullet->getPlayer());
				tank->setLife(tank->getLife() - 1);
				if (tank->getLife() == 0)
				{
					bullet->BigBoom();
				}
				else if(tank->getLevel() == 3)
				{
					tank->setRenderRet(28, 28, (3 - tank->getLife()) * 56, 0);
				}
				if (tank->getAwardAble())
				{
					if (tank->getLevel() == 3)
					{
						tank->setRenderRet(28, 28, 56, 0);
					}
					tank->changeAwardAble();
					nowScene->setAward();
				}
				return;
			}
			else
			{
				bullet->detain();
			}
		}
		else
		{
			if (bullet->getParent() != tank)
			{
				if (tank->getCamp())
				{
					Stop* action = Stop::create(3);
					tank->runAction(action);
					bullet->detain();
					return;
				}
			}
		}
	}
}

void GameManager::UpdateColl(Bullet* bullet1, Bullet* bullet2)
{
	if (bullet1->getCamp() != bullet2->getCamp())
	{
		bullet1->detain();
		bullet2->detain();
		return;
	}
}

void GameManager::UpdateColl(StaticSprite* sp1, Bullet* bullet)
{
	if (sp1->getType() == 1)
	{
		if (bullet->getLevel() == 4)
		{
			sp1->release();
			bullet->SmallBoom();
			return;
		}
		else
		{
			sp1->setPosition(sp1->getPosition() + bullet->getDirection() * 4);
			if (bullet->getDirection().x != 0)
			{
				sp1->setRet(Ret(Size(sp1->getHeight(), sp1->getwidth() - 8), sp1->getPosition()));
				sp1->setRenderRet(sp1->getRenderRet()->RenderSize->getHeight(), sp1->getRenderRet()->RenderSize->getwidth() - 8, 0, 0);
			}
			else
			{
				sp1->setRet(Ret(Size(sp1->getHeight() - 8, sp1->getwidth()), sp1->getPosition()));
				sp1->setRenderRet(sp1->getRenderRet()->RenderSize->getHeight() - 8, sp1->getRenderRet()->RenderSize->getwidth(), 0, 0);
			}
			if (sp1->getHeight() <= 0||sp1->getwidth() <= 0)
			{
				sp1->release();
			}
			bullet->SmallBoom();
			return;
		}
	}
	if (sp1->getType() == 2)
	{
		if (bullet->getLevel() == 4)
		{
			sp1->release();
		}
		bullet->SmallBoom();
		return;
	}
	if (sp1->getType() == 6||
		sp1->getType() == 7 ||
		sp1->getType() == 8 ||
		sp1->getType() == 9)
	{
		nowScene->homeBoom();
	}
}

void GameManager::UpdateColl(Award* aw, Tank* tank)
{
	if (tank->getCamp())
	{
		//award->Play();
		aw->detain();
		if (aw->getType() == 0)
		{
			//LifeAdd->Play();
			setMyLife(getMyLife(tank->getPlayer()) + 1, tank->getPlayer());
			return;
		}
		if (aw->getType() == 1)
		{
			for (int i = 0; i < TankArray::getInstance()->getNumber();i++)
			{
				if (!TankArray::getInstance()->getTankArray()[i]->getCamp())
				{
					Stop* action = Stop::create(StopTime);
					TankArray::getInstance()->getTankArray()[i]->runAction(action);
				}
			}
			return;
		}
		if (aw->getType() == 2)
		{
		}
		if (aw->getType() == 3)
		{
			//boom->Play();
			for (int i = 0; i < TankArray::getInstance()->getNumber();i++)
			{
				if (!TankArray::getInstance()->getTankArray()[i]->getCamp())
				{
					TankArray::getInstance()->getTankArray()[i]->boom();
				}
			}
		}
		if (aw->getType() == 4)
		{
			tank->BulletLevelAdd();
			return;
		}
		if (aw->getType() == 5)
		{
			tank->ChangeInvincible();
			return;
		}
	}
}

