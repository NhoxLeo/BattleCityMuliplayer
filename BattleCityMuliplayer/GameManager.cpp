void GameManager::UpdateColl(StaticSprite* sp1, Bullet* bullet)
{
	if (sp1->getType() == 1)
	{
		if (bullet->getLevel() == 4)
		{
			sp1->release();
			bullet->SmallBoom();
			return;
		}
		else
		{
			sp1->release();
			bullet->SmallBoom();
			if (modNetServer != nullptr) modNetServer->AddDestroyedBrickID(sp1->getID());
			return;
		}
	}
	if (sp1->getType() == 2)
	{
		if (bullet->getLevel() == 4)
		{
			sp1->release();
		}
		bullet->SmallBoom();
		return;
	}
	if (sp1->getType() == 6 || sp1->getType() == 7 || sp1->getType() == 8 || sp1->getType() == 9) nowScene->homeBoom();
}
void GameManager::UpdateColl(Award* aw, Tank* tank)
{
	if (tank->getCamp())
	{
		//award->Play();
		aw->detain();
		if (aw->getType() == 0)
		{
			//LifeAdd->Play();
			setMyLife(getMyLife(tank->getPlayer()) + 1, tank->getPlayer());
			return;
		}
		if (aw->getType() == 1)
		{
			for (int i = 0; i < TankArray::getInstance()->getNumber(); i++)
			{
				if (!TankArray::getInstance()->getTankArray()[i]->getCamp())
				{
					Stop* action = Stop::create(StopTime);
					TankArray::getInstance()->getTankArray()[i]->runAction(action);
				}
			}
			return;
		}
		if (aw->getType() == 2)
		{
		}
		if (aw->getType() == 3)
		{
			//boom->Play();
			for (int i = 0; i < TankArray::getInstance()->getNumber(); i++)
			{
				if (!TankArray::getInstance()->getTankArray()[i]->getCamp())
				{
					TankArray::getInstance()->getTankArray()[i]->boom();
				}
			}
		}
		if (aw->getType() == 4)
		{
			tank->BulletLevelAdd();
			return;
		}
		if (aw->getType() == 5)
		{
			tank->ChangeInvincible();
			return;
		}
	}
}
